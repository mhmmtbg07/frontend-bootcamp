/* logical Operators
    !=
    >
    <
    >=
    <=
*/

// "!="
let age = 18;
console.log(age!=20);

// ">"
console.log(age>10);

// "<"
console.log(age<20);

// banner
console.log("this is a banner");

// ">="
console.log(age>=18);
console.log(age>=20);
console.log(age>=15);

// banner
console.log("this is a banner");

// "<="
console.log(age<=18);
console.log(age<=20);
console.log(age<=15);